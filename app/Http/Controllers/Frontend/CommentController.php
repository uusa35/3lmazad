    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ad = Ad::whereId($request->ad_id)->first();
        $commment = new Comment(['body' => $request->body, 'user_id' => $request->user_id]);
        $element = $ad->comments()->save($commment);
        if ($element) {
            return redirect()->back()->with('success', trans('message.comment_store_success'));
        }
        return redirect()->back()->with('error', trans('message.comment_store_success'));
    }

